    export const getRecommendation = (answers) => {
    const recipient = answers[0].toLowerCase();
    const chocolateType = answers[1].toLowerCase();
    const personality = answers[2].toLowerCase();
    const occasion = answers[3].toLowerCase();
    
    try {
      let recommendation = chocolateRecommendations[recipient]?.[chocolateType]?.[personality]?.[occasion];
      
      if (!recommendation) {
        recommendation = "Milk Chocolate";
      }
    
      return {
        recommendation,
        description: getChocolateDescription(recommendation),
        image: getChocolateImage(recommendation)
      };
    } catch (error) {
      console.error("Error getting recommendation:", error);
      return {
        recommendation: "Milk Chocolate",
        description: "A classic choice that never disappoints!",
        image: "/chocolates/milk-chocolate.png"
      };
    }
    };
    
